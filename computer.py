class Computer:

    def press_on_off(self):
        print("[ Pressing power button...power button pressed ]")
        return self

    def turn_on(self):
        print("[ Computer is turning on - Computer is on ]")
        return self

    def refresh(self):
        print("[ Refreshing computer...computer refreshed ]")
        return self

    def open_file(self):
        print("[ Opening file...File is open ]")
        return self

    def work_on_file(self):
        print("[ Working on file ]")
        return self

    def close_file(self):
        print("[ Closing File... File is close ]")
        return self

    def turn_off(self):
        print("[ Computer is turning off - Computer is off ]")
        return self

